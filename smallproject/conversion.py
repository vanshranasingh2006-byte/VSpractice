def kilometer_to_mile(km):
    return km * 0.621371   
def mile_to_kilometer(mile):
    return mile / 0.621371
def celsius_to_fahrenheit(cel):
    return (cel * 9/5) + 32
def fahrenheit_to_celsius(fah):
    return (fah - 32) * 5/9
def kg_to_pound(kg):
    return kg * 2.20462
def pound_to_kg(pound):
    return pound / 2.20462